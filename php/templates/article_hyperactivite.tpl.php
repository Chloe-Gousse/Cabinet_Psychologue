                                <p><strong>Le Trouble du Déficit de l’Attention, avec ou sans hyperactivité (TDA-H) est un trouble neurodéveloppemental chronique.</strong></p>
                                    
                                <p><span>Il se caractérise par :</span></p>
                                    <p><ul>
                                       <li>- une inattention, des difficultés à se concentrer</li>
                                       <li>- une agitation incessante (hyperactivité)</li>
                                       <li>- une impulsivité marquée</li>
                                    </ul></p>
                                

                                ​
                                <p>Dans le cadre de l’inattention, la personne rencontre des difficultés à trier et à hiérarchiser les diverses informations qui atteignent son cerveau. Il a donc du mal à détourner son attention de stimuli dits « perturbateurs », qui sont le plus souvent liés à l’environnement extérieur (bruits divers, animation, mouvement, etc.), mais parfois aussi d’origine interne (émotions, pensées...). Cela se traduit par une grande distractivité, mais aussi par une tendance à l'hyperfocalisation en particulier pour les tâches stimulantes et susceptibles de lui apporter une gratification immédiate[1].</p>
                                

                                <p>L’hyperactivité motrice est augmentée chez un enfant atteint de TDA/H ; elle est le plus souvent désorganisée et non constructive (agitation permanente, instabilité, nervosité, incapacité à tenir en place). Au plan développemental (donc en grandissant) l’agitation motrice est souvent remplacée par une agitation intérieure (signes ou sentiment de nervosité, d'impatience), ou par une incapacité à rester en place, qui pousse à multiplier les activités, en recherchant même parfois des comportements à risques. Mais il existe aussi une hyperactivité intellectuelle qui se traduit par des pensées abondantes et ininterrompues.<br>

                                L’impulsivité se caractérise par des difficultés à inhiber les actions verbales, motrices, cognitives ou émotionnelles. Il existe aussi une hyperémotivité.<br>

                                
                                ​
                                <div class="article-links">
                                <p>Je pratique le test cognitif TEA-Ch pour les enfants de 6 à 14 ans, qui permet de diagnostiquer un trouble attentionnel et d'identifier quelle modalité est déficitaire.<br>
                                </div>

                                
                                ​

                                <p><span>Trois modalités principales de l'attention sont évaluées : </span>
                                    <ul>
                                        <li>- L’attention soutenue qui est la capacité à se concentrer sur une activité </li>
                                        <li>- L’attention focalisée (sélective) qui est la capacité à résister à la distraction </li>
                                        <li>- Le contrôle attentionnel ou la flexibilité cognitive qui est la capacité à partager son attention entre plusieurs sources distinctes.</li>
                                    </ul>
                                <br>

                                
                                <p><em> Mais ce test doit s’accompagner d’un questionnement clinique, sous forme de bilan passé avec les parents et permettant de valider les informations cognitives recueillies.</em></p>

