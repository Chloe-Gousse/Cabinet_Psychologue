                                <p><strong>Formée à la méditation en pleine conscience et donc la pratiquant quotidiennement depuis 5 ans, j’ai pu relever combien cette pratique attentionnelle peut aider dans la vie de tout un chacun.</strong></p>

                                <p>De plus, il existe des études aux USA qui montrent que cette pratique peut aider les personnes atteintes de TSA . Les études chez les parents de personnes atteintes montrent également des bienfaits.</p>

                                <h3>Quels sont ces bienfaits ?</h3>

                                ​

                                <p>La méditation en pleine conscience consiste en une attention portée sur l’ici et maintenant, sur le corps, les sensations, les pensées et émotions. C’est une manière d’être dans le présent, plutôt que de partir dans le passé (ruminations) et/ou le futur (anxiété).

                                Il est sûr que l’on souhaite être « bien », mais ce n’est pas forcément l’idée… On est <span>là,</span> avec soi-même, et au sein d’un groupe. On accueille ce qui se passe, ce qui est plaisant, mais aussi déplaisant.<br>

                                

                                <h3>Quels outils pour le diagnostic ? </h3>

                                ​

                                <p>Dans le cadre de ces groupes, une partie des séances avec les personnes atteintes va porter sur la notion de « trauma », car beaucoup de gens ont connu ou connaissent encore actuellement des situations les ayant fortement heurtées.</p><br>

