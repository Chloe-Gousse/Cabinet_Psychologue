                                <p><strong>Une personne sur dix souffre de troubles anxieux ou de panique. Entre 20% et 30% des adolescents et des jeunes adultes souffrent d’un trouble anxieux. Ils sont donc fréquents et apparaissent souvent tôt à l’adolescence ou au début de l’âge adulte. Même si les troubles anxieux sont courants, il ne faut pas les prendre à la légère, car ils peuvent être très invalidants. Les troubles anxieux ont comme caractéristiques principales la peur excessive, l’anxiété et l’évitement.
                                </strong></p>

                                <p>Ces troubles ne doivent pas être minimisés, mais si on les prend en charge, alors ils commencent par s’atténuer, pour petit à petit disparaître.</p>


                                <p>Voici les exemples de troubles les plus fréquent chez l'enfant ou le jeune ; bien sûr ils peuvent persister à l’âge adulte, et donc prendre une autre forme, plus « complexe », car souvent persistante.<br>

                                <p>L'anxiété de séparation: anxiété excessive concernant la séparation d'avec la maison ou les personnes auxquelles l'enfant est attaché. Des préoccupations concernant la mort, la leur ou celle des autres, sont aussi fréquentes.<br>
                                ​

                                <p>Refus d'aller à l'école qui peut s’installer et amener à une phobie scolaire.<br>


                                <p><span>Des possibilités de traiter ces problèmes existent ; le psychologue peut travailler en face à face avec l'enfant, et/ou avec l'un (ou les deux) parent(s). C'est de toutes les façons un travail à poursuivre à la maison et il faut se fixer quelques séances pour bien comprendre ce qui se passe !</span>
